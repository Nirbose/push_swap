int	ft_parsing(int n, char **av)
{
	int	j;
	int	i;

	j = 0;
	i = 0;
	while (j < n)
	{
		while (av[j][i] != '\0')
		{
			if (ft_isdigit(av[j][i]) == 0)
				return (0);
			i++;
		}
		j++;
	}
}

